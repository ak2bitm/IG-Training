package com.akhilesh.HasARelationship;

public class Address {

	String city;
	String state;
	String country;
	int pin;
	public Address() {}
	public Address(String city, String state, String country, int pin) {
		super();
		this.city = city;
		this.state = state;
		this.country = country;
		this.pin = pin;
	}
	
	
}
